    <br>
    <p>1. 通用组件可以进一步封装为业务组件，业务组件中可以抽象出通用组件</p>
    <p>2. 通用组件更接近于纯函数，可以单独测试、单独导出、四处移植；业务组件与业务逻辑耦合，不具备通用性</p>
    <p>3. 通用组件仅包含最稳定的逻辑，不稳定的部分需要通过参数配置、定制 slot 或外部进一步封装实现；业务组件更接近最终可用状态</p>
    <p>4. 通用组件的关注点是分离变化与不变的部分，业务组件关注业务实现</p>
