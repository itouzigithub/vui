    <p>通用组件层必须是最稳定的</p>
    <p>由于任何修改都会影响所有依赖，因此需要考虑向后兼容</p>
    <p>通用组件层中的组件并不是完全独立的，因为即便是通用组件，仍然会存在公用的样式或方法，也就是说通用组件尽管依赖最小，仍然可以抽象出一个基础样式库 base.css 和基础方法库 util.js</p>
